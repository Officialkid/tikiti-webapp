declare global {
  namespace Cypress {
    interface Chainable {
      /**
       * Custom command to log in as a test user
       * @example cy.login('test@example.com', 'password123')
       */
      login(email: string, password: string): Chainable<void>;
      
      /**
       * Custom command to add an event to cart
       * @example cy.addToCart('event-id-123')
       */
      addToCart(eventId: string): Chainable<void>;
    }
  }
}

// Login command
Cypress.Commands.add('login', (email: string, password: string) => {
  cy.visit('/login');
  cy.get('input[type="email"]').type(email);
  cy.get('input[type="password"]').type(password);
  cy.get('button[type="submit"]').click();
  cy.url().should('not.include', '/login');
});

// Add to cart command
Cypress.Commands.add('addToCart', (eventId: string) => {
  cy.visit(`/events/${eventId}`);
  cy.contains('Add to Cart').click();
  cy.contains('Added to cart').should('be.visible');
});

export {};
